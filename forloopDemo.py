#The purpose of this program is to demonstrate the use of a for loop

#Create a for loop to iterate specified numbers
#First parameter in loop specifies what number to start at
#Second parameter in loop specifies where to end
#Third parameter specifies what to count by
#Use end='' to print first statement on same line
#Output That's all folks! (use \n to output on new line)
for number in range(3, 31, 3):
        print(str(number)+' ', end='')

print("\nThat's all folks!")


    
    
    
